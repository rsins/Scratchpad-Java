package com.myexample.junit;

public class Calculator {
	public int add(int aInput1, int aInput2) {
		return aInput1 + aInput2;
	}
	
	public int subtract(int aInput1, int aInput2) {
		return aInput1 - aInput2;
	}
	
	public int multiply(int aInput1, int aInput2) {
		return aInput1 * aInput2;
	}
	
	public int divide(int aInput1, int aInput2) {
		return aInput1 / aInput2;
	}
}
